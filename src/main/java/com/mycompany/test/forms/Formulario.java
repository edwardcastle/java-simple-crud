    private static List<Client> lista = new ArrayList<>();

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int index = this.nameList.getSelectedIndex();
        Client client = lista.get(index);
        ClienteDao clienteDao = new ClienteDao();
        clienteDao.deleteClient(client.getId());
        updateList();
    }//GEN-LAST:event_btnDeleteActionPerformed
    private void updateList() {
        ClienteDao clienteDao = new ClienteDao();
        lista = clienteDao.listClient();
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < lista.size(); i++) {
            Client client = lista.get(i);
            model.addElement(client.getName() + " " + client.getLastName());
        }
        this.nameList.setModel(model);
    }
